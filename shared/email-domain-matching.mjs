// Normalizes a person name for fuzzy matching: strips honorifics,
// post-comma credentials ("Jane Doe, LMFT"), and punctuation noise.
export function normalizeNameForMatch(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/^(dr|mr|mrs|ms|mx|prof)\.?\s+/i, "")
    .split(",")[0]
    .replace(/[^a-z\s'-]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

